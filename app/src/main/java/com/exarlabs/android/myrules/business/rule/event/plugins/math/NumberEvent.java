package com.exarlabs.android.myrules.business.rule.event.plugins.math;

import com.exarlabs.android.myrules.business.rule.event.Event;

/**
 * Example implementation of an Event which delivers an integer number
 * Created by becze on 1/11/2016.
 */
public class NumberEvent extends Event {

    // ------------------------------------------------------------------------
    // TYPES
    // ------------------------------------------------------------------------

    // ------------------------------------------------------------------------
    // STATIC FIELDS
    // ------------------------------------------------------------------------

    private static final String TAG = NumberEvent.class.getSimpleName();


    // ------------------------------------------------------------------------
    // STATIC METHODS
    // ------------------------------------------------------------------------

    // ------------------------------------------------------------------------
    // FIELDS
    // ------------------------------------------------------------------------

    private int mValue;


    // ------------------------------------------------------------------------
    // CONSTRUCTORS
    // ------------------------------------------------------------------------


    // ------------------------------------------------------------------------
    // METHODS
    // ------------------------------------------------------------------------

    @Override
    public String toString() {
        return TAG + "(Value: " + getValue() + ")";
    }


    // ------------------------------------------------------------------------
    // GETTERS / SETTTERS
    // ------------------------------------------------------------------------


    public int getValue() {
        return mValue;
    }

    public void setValue(int value) {
        mValue = value;
    }
}
